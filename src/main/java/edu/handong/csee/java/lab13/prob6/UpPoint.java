public class UpPoint {//this class is UpPoint
	private int x, y; //variable of x,y point

	UpPoint(int x, int y){//constructor of UpPoint class
		this.x = x; //this class x is initiate to x
		this.y = y;//this class y is initiate to y
	}

	public String toString(){//this method return String type
		return "x : " + x + " y : " + y;//return "x : " + x + " y : " + y
	}
}
